import numpy as np

# Create a simple NumPy array
data = np.array([10, 20, 30, 40, 50])

# Perform basic operations
array_sum = np.sum(data)  # Sum of array elements
array_mean = np.mean(data)  # Mean of array elements
array_max = np.max(data)  # Maximum value in the array

# Print results
print("Array:", data)
print("Sum of elements:", array_sum)
print("Mean of elements:", array_mean)
print("Maximum value:", array_max)
